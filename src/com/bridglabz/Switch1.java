package com.bridglabz;

public class Switch1 {
    public static void main(String[] args){
        char ch='I';
       switch (ch){
           case 'A':
               System.out.println(ch+"==>is vowel");
               break;
           case 'E':
                   System.out.println("==>is vowel");
                   break;
           case 'I':
               System.out.println("==>is vowel");
               break;
           case 'O':
               System.out.println("==>is vowel");
               break;
           case 'U':
               System.out.println("==>is vowel");
               break;
           default:
               System.out.println("==>is a consonants");
               break;

       }

    }
}
